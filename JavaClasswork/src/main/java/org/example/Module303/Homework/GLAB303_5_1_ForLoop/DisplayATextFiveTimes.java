package org.example.Module303.Homework.GLAB303_5_1_ForLoop;

public class DisplayATextFiveTimes {
    //program to print a text five times
    public static void main(String[] args) {
        int n = 5;
        // for loop
        for (int i = 1; i <= n; ++i) {
            System.out.println("Java is fun");
        }
    }
}
