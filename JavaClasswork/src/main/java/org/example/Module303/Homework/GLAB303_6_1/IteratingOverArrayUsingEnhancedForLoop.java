package org.example.Module303.Homework.GLAB303_6_1;

public class IteratingOverArrayUsingEnhancedForLoop {
    public static void main(String[] args) {
        String[] names = {"New York", "Dallas", "Las Vegas", "Florida"};
        for (String name : names) {
            System.out.println(name);
        }
    }
}


//        Output
//        New York
//        Dallas
//        Las Vegas
//        Florida



